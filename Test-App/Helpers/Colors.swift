import Foundation
import UIKit


extension UIColor {
    
    func primaryColor() -> UIColor {
        
        return UIColor(red: 0.03, green: 0.42, blue: 0.66, alpha: 1.00)
        
    }
    
    func tabBarSelectedColor() -> UIColor {
        return UIColor(red: 255/255, green: 216/255, blue: 223/255, alpha: 1)
    }
}

